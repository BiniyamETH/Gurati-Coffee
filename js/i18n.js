  const LANG_HTML_CODE = { en: 'en', am: 'am', om: 'om' };

  function applyLang(lang) {
    const dict = TRANSLATIONS[lang] || TRANSLATIONS.en;

    document.querySelectorAll('[data-i18n]').forEach((el) => {
      const key = el.getAttribute('data-i18n');
      const val = dict[key];
      if (val == null) return;
      if (el.hasAttribute('data-i18n-html')) {
        el.innerHTML = val;
      } else {
        el.textContent = val;
      }
    });

    document.querySelectorAll('[data-i18n-placeholder]').forEach((el) => {
      const key = el.getAttribute('data-i18n-placeholder');
      const val = dict[key];
      if (val != null) el.setAttribute('placeholder', val);
    });

    document.documentElement.setAttribute('lang', LANG_HTML_CODE[lang] || 'en');
    document.body.classList.toggle('lang-am', lang === 'am');

    document.querySelectorAll('.lang-switcher button').forEach((btn) => {
      btn.classList.toggle('active', btn.getAttribute('data-lang') === lang);
    });

    try {
      localStorage.setItem(STORAGE_KEY, lang);
    } catch (e) {
      /* localStorage unavailable — language just won't persist across pages */
    }
  }

  function initLangSwitchers() {
    document.querySelectorAll('.lang-switcher button').forEach((btn) => {
      btn.addEventListener('click', () => applyLang(btn.getAttribute('data-lang')));
    });
  }

  let savedLang = 'en';
  try {
    savedLang = localStorage.getItem(STORAGE_KEY) || 'en';
  } catch (e) {
    savedLang = 'en';
  }

  initLangSwitchers();
  applyLang(savedLang);
